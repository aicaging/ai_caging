def deploy_cagingcli(data_dir: Path, child_name: str, parent_url: str, api_key: str, owner: str,
                     params: dict = None, parent_params: dict = None):
    """Deploy cagingcli.py and cagingcli.yaml (replaces inline bash in init.sh)."""
    cli_dir = data_dir / "cli"
    cli_dir.mkdir(parents=True, exist_ok=True)
    subprocess.run(["chown", f"{owner}:{owner}", str(cli_dir)], check=True)

    # Copy cagingcli.py from cli subdirectory
    src_cli = SCRIPT_DIR / "cli" / "cagingcli.py"
    if src_cli.exists():
        shutil.copy2(str(src_cli), str(cli_dir / "cagingcli.py"))
        subprocess.run(["chown", f"{owner}:{owner}", str(cli_dir / "cagingcli.py")], check=True)
        (cli_dir / "cagingcli.py").chmod(0o750)
        print(f"  cagingcli.py -> {cli_dir / 'cagingcli.py'}")
    else:
        print(f"  WARNING: cagingcli.py not found at {src_cli}, skipping")

    # Copy CLI support files (shell scripts, docs)
    for filename in ("cagingcli.md", "exec.sh", "protect.sh", "release.sh"):
        src_file = SCRIPT_DIR / "cli" / filename
        if src_file.exists():
            shutil.copy2(str(src_file), str(cli_dir / filename))
            subprocess.run(["chown", f"{owner}:{owner}", str(cli_dir / filename)], check=True)
            (cli_dir / filename).chmod(0o750)
            print(f"  {filename} -> {cli_dir / filename}")
        else:
            print(f"  WARNING: {filename} not found at {src_file}, skipping")

    # Create cagingcli.yaml
    cli_yaml_path = cli_dir / "cagingcli.yaml"
    cli_config = {
        "parent": {
            "base_url": parent_url,
            "api_key": api_key,
            "timeout": 30,
        },
        "defaults": {
            "output": "text",
        },
    }
    if params:
        cli_config["params"] = dict(params)
    if parent_params:
        cli_config["parent_params"] = dict(parent_params)
    with open(cli_yaml_path, "w") as f:
        yaml.dump(cli_config, f, default_flow_style=False)
    subprocess.run(["chown", f"{owner}:{owner}", str(cli_yaml_path)], check=True)
    cli_yaml_path.chmod(0o640)
    print(f"  cagingcli.yaml -> {cli_yaml_path}")


