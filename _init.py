API_KEY_PREFIX = "ak_live_"
API_KEY_BYTES = 24


def generate_api_key() -> tuple:
    """Generate a new API key and its bcrypt hash. Returns (plain_key, hashed_key)."""
    raw = secrets.token_hex(API_KEY_BYTES)
    plain_key = f"{API_KEY_PREFIX}{raw}"
    hashed_key = bcrypt.hashpw(plain_key.encode(), bcrypt.gensalt()).decode()
    return plain_key, hashed_key


