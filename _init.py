def setup_db_credentials(data_dir: Path, username: str, password: str, db_path: str):
    """Replicates old_install.py logic: init DB, create user with bcrypt-hashed credentials."""
    sys.path.insert(0, str(SCRIPT_DIR))
    from caging import database as db

    db.init_db(db_path)

    raw_key = username
    password_hash = bcrypt.hashpw(password.encode(), bcrypt.gensalt()).decode()
    hashed_key = bcrypt.hashpw(raw_key.encode(), bcrypt.gensalt()).decode()

    existing = db.get_user(username)
    if existing:
        db.update_user(username, password_hash=password_hash)
        print(f"  Updated existing user: {username}")
    else:
        db.create_user(
            client_id=username,
            role="admin",
            api_key_hash=hashed_key,
            system_user=username,
        )
        db.update_user(username, password_hash=password_hash)
        print(f"  Created user: {username}")
    print(f"  DB credentials set for: {username}")


