# Detect venv Python for systemd services
def _detect_venv_python() -> str:
    """Return the Python interpreter from the current venv for systemd services."""
    # 1. If running inside a venv, use sys.executable
    if sys.prefix != sys.base_prefix:
        return sys.executable
    # 2. Check VIRTUAL_ENV environment variable
    venv = os.environ.get("VIRTUAL_ENV")
    if venv:
        candidate = Path(venv) / "bin" / "python3"
        if candidate.exists():
            return str(candidate)
    # 3. Search for a nearby .venv directory (common project structure)
    for candidate_dir in [
        SCRIPT_DIR.parent.parent / ".venv",   # project root (GenericAgent/.venv)
        SCRIPT_DIR.parent / ".venv",           # temp/.venv
        SCRIPT_DIR / ".venv",                  # caging/.venv
    ]:
        candidate = candidate_dir / "bin" / "python3"
        if candidate.exists():
            return str(candidate)
    # 4. Fall back to system python
    return shutil.which("python3") or "/usr/bin/python3"


