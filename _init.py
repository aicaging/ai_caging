def parse_args(argv=None):
    parser = argparse.ArgumentParser(
        description="Caging deployment orchestrator",
        formatter_class=argparse.RawDescriptionHelpFormatter,
        epilog=__doc__,
    )
    parser.add_argument("--plan", default=str(SCRIPT_DIR / "plan.yaml"),
                        help="Path to plan.yaml (default: ./plan.yaml)")
    parser.add_argument("--user", nargs="+", default=None,
                        help="Deploy only specific user(s) from plan")
    parser.add_argument("--link", nargs=3, metavar=('MODE', 'PARENT', 'CHILD'),
                        help="Deploy a single parent→child link (MODE=service|cage)")
    parser.add_argument("--port", type=int, default=8000,
                        help="Port for the child service (link mode)")
    parser.add_argument("--username", default=None,
                        help="Web UI username for service mode (link mode)")
    parser.add_argument("--password", default=None,
                        help="Web UI password for service mode (link mode)")
    parser.add_argument("--dry-run", action="store_true",
                        help="Simulate deployment (no system changes)")
    parser.add_argument("--nginx", action="store_true",
                        help="Generate nginx portal config from plan.yaml")
    parser.add_argument("--portal-port", type=int, default=50080,
                        help="Nginx portal listen port (default: 50080)")
    parser.add_argument("--restart-services", action="store_true", default=True,
                        help="Restart systemd services after deployment (default: True)")
    return parser.parse_args(argv)


def main():
    args = parse_args()

    if os.geteuid() != 0 and not args.dry_run:
        print("ERROR: Must run as root (sudo)")
        sys.exit(1)

    plan_path = args.plan
    if not os.path.exists(plan_path):
        print(f"ERROR: Plan file not found: {plan_path}")
        sys.exit(1)

    # ── Nginx mode ─────────────────────────────────────────────────
    if args.nginx:
        print(f"\n{'='*60}")
        print(" Nginx portal setup")
        print(f"{'='*60}")
        setup_nginx(plan_path, portal_port=args.portal_port, dry_run=args.dry_run)
        return

    # Always load plan.yaml for AI defaults and existing user configs
    print(f"Plan: {plan_path}")
    ordered_users, all_users = load_plan(plan_path)
    print(f"Users in deployment order: {[u['name'] for u in ordered_users]}")

    # ── Single-link mode ────────────────────────────────────────────
    if args.link:
        mode, parent_name, child_name = args.link
        print(f"\n{'='*60}")
        print(f" Single-link deploy: {mode} {parent_name} → {child_name}")
        print(f"{'='*60}")

        # AI defaults: from plan.yaml's global ai section
        plan_data = yaml.safe_load(open(plan_path))
        global_ai = plan_data.get("ai", {})
        if not global_ai.get("api_key"):
            global_ai.setdefault("api_key", "")
            global_ai.setdefault("provider", "openai")
            global_ai.setdefault("model", "deepseek-reasoner")
            global_ai.setdefault("base_url", "https://api.deepseek.com")
            global_ai.setdefault("fallback", "manual")

        # Resolve parent config: use plan.yaml entry if exists, else create one
        if parent_name in all_users:
            parent_entry = dict(all_users[parent_name])
            print(f"  Parent '{parent_name}' found in plan.yaml")
        else:
            parent_entry = {
                "name": parent_name,
                "mode": "service",
                "port": 8000,
                "parent": {"user": None},
                "children": [child_name],
                "human_login": {"username": args.username or parent_name,
                                "password": args.password or "changeit!"},
                "ai": dict(global_ai),
                "params": {},
            }
            print(f"  Parent '{parent_name}' created from defaults")

        # Create child entry
        child_entry = {
            "name": child_name,
            "mode": mode,
            "port": args.port if mode == "service" else 0,
            "parent": {"user": parent_name},
            "children": [],
            "ai": dict(global_ai),
            "params": {},
        }
        # Merge any existing plan.yaml AI config for child
        if child_name in all_users:
            child_entry["ai"] = dict(all_users[child_name].get("ai", global_ai))
        if mode == "service":
            child_entry["human_login"] = {
                "username": args.username or child_name,
                "password": args.password or "changeit!",
            }

        # Build user dict
        deploy_users = {parent_name: parent_entry, child_name: child_entry}

        if args.dry_run:
            print(f"\n  Would deploy:")
            print(f"    {parent_name}: mode={parent_entry['mode']}, port={parent_entry.get('port')}")
            print(f"    {child_name}: mode={child_entry['mode']}, port={child_entry.get('port')}")
            print(f"\n  (Dry run complete — no changes made)")
            return

        # Deploy parent first, then child
        stop_existing_services()
        deploy_user(parent_entry, deploy_users)
        deploy_user(child_entry, deploy_users)
        print(f"\n✓ Link deployed: {parent_name} → {child_name}")
        return

    # ── Plan-based mode ─────────────────────────────────────────────
    if args.user:
        ordered_users = [u for u in ordered_users if u["name"] in args.user]
        if not ordered_users:
            print(f"ERROR: No matching users found for: {args.user}")
            sys.exit(1)
        print(f"Filtered to: {[u['name'] for u in ordered_users]}")

    if args.dry_run:
        print("\n=== DRY RUN ===")
        print(f"Would deploy: {[u['name'] for u in ordered_users]}")
        for u in ordered_users:
            mode = u.get("mode", "cage")
            port = u.get("port", 8000)
            parent_info = u.get("parent", {})
            parent_name = parent_info.get("user") if isinstance(parent_info, dict) else None
            print(f"  {u['name']}: mode={mode}, port={port}, parent={parent_name}")
        print("\n(Dry run complete — no changes made)")
        return

    # Stop existing services
    stop_existing_services()

    # Deploy each user in topological order
    for user in ordered_users:
        deploy_user(user, all_users)

    # Final summary
    print(f"\n{'='*60}")
    print(f" Deployment complete")
    print(f"{'='*60}")
    for name, _ in all_users.items():
        deployed = name in [u["name"] for u in ordered_users]
        print(f"  {name}: {'✓' if deployed else '— (skipped)'}")
    print(f"\n  Start services:   systemctl start caging@<user>")
    print(f"  View logs:        journalctl -u caging@<user> -f")


if __name__ == "__main__":
    main()
