def load_plan(plan_path: str) -> tuple:
    """Load and parse plan.yaml, return (topologically_ordered_users, raw_users_dict)."""
    with open(plan_path) as f:
        data = yaml.safe_load(f)

    global_ai = data.get("ai", {})
    users_raw = data.get("users", {})

    # Merge AI config into each user (user-level overrides global)
    users = {}
    for name, attrs in users_raw.items():
        user_ai = dict(global_ai)
        user_ai.update(attrs.get("ai", {}))
        user = dict(attrs)
        user["name"] = name
        user["ai"] = user_ai
        user["parent"] = attrs.get("parent", {})
        user["children"] = attrs.get("children", [])
        user["human_login"] = attrs.get("human_login")
        user["params"] = attrs.get("params", {})
        user["mode"] = attrs.get("mode", "cage")
        user["port"] = attrs.get("port", 8000)
        users[name] = user

    # Topological sort: parents before children
    ordered = []
    added = set()

    def add_user(name):
        if name in added:
            return
        added.add(name)
        user = users.get(name)
        if user:
            parent_info = user.get("parent", {})
            parent_name = parent_info.get("user") if isinstance(parent_info, dict) else None
            if parent_name and parent_name in users and parent_name not in added:
                add_user(parent_name)
            ordered.append(user)

    for name in users:
        add_user(name)

    return ordered, users


def get_parent_url(parent_name: str, parent_port: int) -> str:
    """Build parent base URL."""
    return f"http://localhost:{parent_port}"


def create_system_user(name: str, caging_base: Path):
    """Create Linux system user for caging."""
    home = caging_base if name == "root" else caging_base / name
    if subprocess.run(["id", name], capture_output=True).returncode != 0:
        subprocess.run(
            ["useradd", "--system", "--no-create-home", "--home-dir", str(home),
             "--shell", "/bin/bash", name],
            check=True,
        )
        print(f"  System user created: {name}")
    else:
        # Ensure shell is correct even for existing users
        subprocess.run(
            ["usermod", "--shell", "/bin/bash", name],
            check=True,
        )
        print(f"  System user exists, shell updated: {name}")


def create_data_dir(name: str, data_dir: Path):
    """Create ~/.caging data directory with proper ownership."""
    data_dir.mkdir(parents=True, exist_ok=True)
    owner = "root" if name == "root" else name
    subprocess.run(["chown", f"{owner}:{owner}", str(data_dir)], check=True)
    data_dir.chmod(0o750)
    print(f"  Data dir: {data_dir}")


def generate_config_yaml(user: dict, data_dir: Path) -> dict:
    """Generate the per-user config.yaml as a dict."""
    port = user.get("port", 8000)
    child_name = user["name"]
    db_path = str(data_dir / "caging.db")

    config = yaml.safe_load(CONFIG_TEMPLATE.format(
        port=port,
        db_path=db_path,
        child_name=child_name,
    ))

    # AI section
    ai_cfg = user.get("ai", {})
    config["ai"] = {
        "provider": ai_cfg.get("provider", "openai"),
        "api_key": ai_cfg.get("api_key", ""),
        "model": ai_cfg.get("model", "deepseek-reasoner"),
        "base_url": ai_cfg.get("base_url", "https://api.deepseek.com"),
        "fallback": ai_cfg.get("fallback", "manual"),
    }

    # Parent section (if user has a parent)
    parent_info = user.get("parent", {})
    parent_name = parent_info.get("user") if isinstance(parent_info, dict) else None
    if parent_name:
        config["parent"] = {
            "enabled": True,
            "base_url": f"http://localhost:{_get_user_port(parent_name)}",
            "api_key": "",
            "timeout": 30,
            "poll_interval_seconds": 10,
        }
    else:
        config["parent"] = {
            "enabled": False,
            "base_url": "",
            "api_key": "",
            "timeout": 30,
            "poll_interval_seconds": 10,
        }

    # Params from plan.yaml
    params = user.get("params", {})
    if params:
        config["params"] = dict(params)
    parent_info = user.get("parent", {})
    if isinstance(parent_info, dict):
        parent_params = parent_info.get("param", {})
        if parent_params:
            config["parent_params"] = dict(parent_params)

    return config


def write_config_yaml(config: dict, config_path: Path, owner: str):
    """Write config.yaml and set ownership."""
    with open(config_path, "w") as f:
        yaml.dump(config, f, default_flow_style=False)
    subprocess.run(["chown", f"{owner}:{owner}", str(config_path)], check=True)
    config_path.chmod(0o640)
    print(f"  config.yaml -> {config_path}")


def _get_user_port(username: str) -> int:
    """Fetch port for a user from the global registry."""
    return user_registry.get(username, {}).get("port", 8000)


