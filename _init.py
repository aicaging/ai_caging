def deploy_user(user: dict, all_users: dict):
    """Deploy one user: OS user, data dir, config, keys, DB, systemd, CLI, ACL."""
    name = user["name"]
    mode = user.get("mode", "cage")
    port = user.get("port", 8000)
    parent_info = user.get("parent", {})
    parent_name = parent_info.get("user") if isinstance(parent_info, dict) else None
    children = user.get("children", [])
    human_login = user.get("human_login")
    params = user.get("params", {})
    ai_cfg = user.get("ai", {})

    # Register this user
    user_registry[name] = user

    print(f"\n{'='*60}")
    print(f" Deploying: {name} (mode={mode}, port={port})")
    print(f"{'='*60}")

    # Paths
    child_home = CAGING_BASE if name == "root" else CAGING_BASE / name
    child_data = child_home / ".caging"
    child_config_path = child_data / "config.yaml"

    # Resolve parent
    if parent_name:
        parent_port = all_users.get(parent_name, {}).get("port", 8000)
        parent_url = f"http://localhost:{parent_port}"
        parent_data = CAGING_BASE if parent_name == "root" else CAGING_BASE / parent_name
        parent_data_path = parent_data / ".caging"
        parent_config_path = parent_data_path / "config.yaml"
    else:
        parent_url = ""
        parent_config_path = None

    # 1. Create system user
    create_system_user(name, CAGING_BASE)

    # 2. Create data directory
    create_data_dir(name, child_data)

    # 3. Config + policy (only for service mode; cage is CLI-only)
    owner = "root" if name == "root" else name
    if mode == "service":
        config = generate_config_yaml(user, child_data)
        write_config_yaml(config, child_config_path, owner)
        deploy_policy(child_data, owner)
    else:
        print(f"  (cage mode — no config.yaml/policy.yaml needed)")

    # Extract params for config files
    params = user.get("params", {})
    parent_params = {}
    if isinstance(parent_info, dict):
        parent_params = parent_info.get("param", {}) or {}

    # 4. API keys — inject child's key into parent config
    child_key = None
    if parent_name and parent_config_path and parent_config_path.exists():
        with open(parent_config_path) as f:
            parent_config = yaml.safe_load(f)
        child_key, child_key_hash = generate_api_key()
        # Add child's key to parent's api_keys
        if "auth" not in parent_config:
            parent_config["auth"] = {}
        if "api_keys" not in parent_config["auth"]:
            parent_config["auth"]["api_keys"] = {}
        parent_config["auth"]["api_keys"][name] = {
            "role": "requester",
            "system_user": name,
            "key": child_key,
        }
        with open(parent_config_path, "w") as f:
            yaml.dump(parent_config, f, default_flow_style=False)
        print(f"  API key injected into parent ({parent_name}) for child ({name})")

        # For service mode child: also inject child's own key into its config
        if mode == "service" and child_config_path.exists():
            with open(child_config_path) as f:
                child_config = yaml.safe_load(f)
            child_config["parent"]["api_key"] = child_key
            with open(child_config_path, "w") as f:
                yaml.dump(child_config, f, default_flow_style=False)
            print(f"  Child API key injected into {name} config.yaml")
    elif parent_name and parent_config_path:
        print(f"  WARNING: Parent config not found at {parent_config_path}, skipping API key injection")

    # 5. DB credentials (only for service mode)
    if mode == "service" and human_login:
        db_path = str(child_data / "caging.db")
        print(f"\n  Setting up Web UI login credentials...")
        setup_db_credentials(
            data_dir=child_data,
            username=human_login.get("username", name),
            password=human_login.get("password"),
            db_path=db_path,
        )
        # Fix ownership
        subprocess.run(["chown", f"{owner}:{owner}", db_path], check=True)

    # 6. Systemd service (only for service mode)
    if mode == "service":
        setup_systemd_service(name, port, child_data, owner=owner)

    # 7. Caging CLI deployment (only if this user has a parent to connect to)
    if parent_name:
        if child_key is None:
            child_key, _ = generate_api_key()
        deploy_cagingcli(child_data, name, parent_url, child_key, owner,
                         params=params, parent_params=parent_params)

    # 8. ACL setup
    if parent_name:
        setup_acl(parent_name, child_home)

    # 9. Recursive ownership fix for the entire .caging directory
    #    Ensures database and any runtime-created files are owned by the service user
    subprocess.run(["chown", "-R", f"{owner}:{owner}", str(child_data)], check=True)
    print(f"  Ownership fixed: {child_data} -> {owner}")

    # Summary
    print(f"\n  ✓ {name} deployed successfully")


