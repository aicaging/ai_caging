SYSTEMD_TEMPLATE = """[Unit]
Description=Caging {name}
After=network.target

[Service]
Type=simple
User={name}
Group={name}
WorkingDirectory={data_dir}
ExecStart={python_path} -m uvicorn caging.app:app --host 0.0.0.0 --port {port}
Restart=on-failure
RestartSec=5
Environment=PYTHONUNBUFFERED=1
Environment=PYTHONPATH={caging_src}
Environment=CAGING_CONFIG={config_path}

[Install]
WantedBy=multi-user.target
"""


def setup_systemd_service(name: str, port: int, data_dir: Path, owner: str = "root"):
    """Create systemd service file for a caging user (replaces inline bash in init.sh).

    Uses the current venv Python and treats the caging source directory
    as a shared code base via PYTHONPATH.
    """
    python_path = VENV_PYTHON
    caging_src = str(SCRIPT_DIR)  # shared caging code source (contains caging/ package)
    config_path = str(data_dir / "config.yaml")

    svc_content = SYSTEMD_TEMPLATE.format(
        name=name,
        data_dir=str(data_dir),
        python_path=python_path,
        port=port,
        config_path=config_path,
        caging_src=caging_src,
    )

    svc_path = Path(f"/etc/systemd/system/caging@{name}.service")
    with open(svc_path, "w") as f:
        f.write(svc_content)
    svc_path.chmod(0o644)
    print(f"  systemd unit: {svc_path}")

    # Enable and (re)start
    subprocess.run(["systemctl", "daemon-reload", "-q"], check=False)
    subprocess.run(["systemctl", "enable", f"caging@{name}"], check=False)
    subprocess.run(["systemctl", "restart", f"caging@{name}"], check=False)
    print(f"  service caging@{name} started")


