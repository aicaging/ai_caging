def setup_nginx(plan_path: str, portal_port: int = 50080, dry_run: bool = False):
    """Generate nginx portal config from plan.yaml, then validate and reload.

    Creates one location per service-mode user:
      /{username} → http://localhost:{port}/ui
    """
    if not os.path.exists(plan_path):
        print(f"ERROR: Plan file not found: {plan_path}")
        sys.exit(1)

    with open(plan_path) as f:
        plan = yaml.safe_load(f)

    users = plan.get("users", {})
    # Collect service-mode users (have a web service)
    service_users = []
    for name, cfg in users.items():
        if cfg.get("mode") == "service" and cfg.get("port"):
            service_users.append((name, cfg["port"]))
    service_users.sort(key=lambda x: x[1])  # sort by port

    if not service_users:
        print("ERROR: No service-mode users found in plan.yaml")
        sys.exit(1)

    dashboard_dir = CAGING_BASE / "dashboard"
    nginx_conf = Path("/etc/nginx/sites-available/caging")
    nginx_enabled = Path("/etc/nginx/sites-enabled/caging")

    print(f"  Portal port: {portal_port}")
    print(f"  Dashboard:   {dashboard_dir}")
    print(f"  Nginx conf:  {nginx_conf}")
    print(f"  Service users:")
    for name, port in service_users:
        print(f"    /{name:10} → http://localhost:{port}/ui")

    # ── 1. Copy dashboard HTML ──────────────────────────────────────
    src_dashboard = SCRIPT_DIR / "dashboard.html"
    if not src_dashboard.exists():
        print(f"ERROR: dashboard.html not found at {src_dashboard}")
        sys.exit(1)

    if not dry_run:
        dashboard_dir.mkdir(parents=True, exist_ok=True)
        dst_index = dashboard_dir / "index.html"
        shutil.copy2(str(src_dashboard), str(dst_index))
        # Set ownership to root
        subprocess.run(["chown", "-R", "root:root", str(dashboard_dir)], check=True)
        subprocess.run(["chmod", "755", str(dashboard_dir)], check=True)
        subprocess.run(["chmod", "644", str(dst_index)], check=True)
        print(f"  [✓] Dashboard copied to {dst_index}")

    # ── 2. Build nginx config ──────────────────────────────────────
    location_blocks = []
    for name, port in service_users:
        upstream = f"http://localhost:{port}"
        block = f"""    # ── {name} panel (port {port}) ──
    location /{name} {{
        proxy_pass {upstream}/ui;
        proxy_http_version 1.1;
        proxy_set_header Upgrade $http_upgrade;
        proxy_set_header Connection "upgrade";
        proxy_set_header Host $host;
        proxy_set_header X-Real-IP $remote_addr;
        proxy_set_header X-Forwarded-For $proxy_add_x_forwarded_for;
        proxy_set_header X-Forwarded-Proto $scheme;

        # Strip /{name} prefix before passing upstream
        rewrite ^/{name}(.*)$ $1 break;
    }}
"""
        location_blocks.append(block)

    locations_text = "\n".join(location_blocks)

    config_text = f"""server {{
    listen {portal_port} default_server;
    listen [::]:{portal_port} default_server;

    server_name _;

    # Dashboard static files
    root {dashboard_dir};
    index index.html;

    # Logging
    access_log /var/log/nginx/caging_access.log;
    error_log  /var/log/nginx/caging_error.log;

{locations_text}
    # ── Static assets (for dashboard CSS/images) ──
    location /static {{
        alias {dashboard_dir}/static;
        expires 7d;
    }}

    # ── Deny access to hidden files ──
    location ~ /\\. {{
        deny all;
    }}
}}
"""

    if dry_run:
        print(f"\n=== Nginx config (dry-run) ===\n{config_text}")
        print("(Dry run complete — no changes made)")
        return

    # ── 3. Write config ────────────────────────────────────────────
    with open(str(nginx_conf), "w") as f:
        f.write(config_text)
    print(f"  [✓] Nginx config written to {nginx_conf}")

    # ── 4. Activate site ───────────────────────────────────────────
    nginx_enabled.parent.mkdir(parents=True, exist_ok=True)
    if nginx_enabled.exists() or nginx_enabled.is_symlink():
        nginx_enabled.unlink()
    nginx_enabled.symlink_to(str(nginx_conf))
    print(f"  [✓] Site enabled: {nginx_enabled} → {nginx_conf}")

    # Remove default site if it conflicts
    default_site = Path("/etc/nginx/sites-enabled/default")
    if default_site.exists():
        print("  [*] Removing default nginx site (port conflict)...")
        default_site.unlink()

    # ── 5. Validate and reload ─────────────────────────────────────
    print("  [*] Validating nginx config...")
    result = subprocess.run(["nginx", "-t"], capture_output=True, text=True)
    if result.returncode != 0:
        print(f"  ✗ Nginx config validation FAILED:")
        print(result.stdout)
        print(result.stderr)
        sys.exit(1)
    print("  [✓] Nginx config valid")

    print("  [*] Reloading nginx...")
    subprocess.run(["systemctl", "reload", "nginx"],
                   capture_output=True) or subprocess.run(
        ["systemctl", "restart", "nginx"], capture_output=True)

    print(f"\n{'='*60}")
    print(f"  ✅ Caging Portal ready!")
    print(f"  🌐 http://localhost:{portal_port}")
    for name, port in service_users:
        print(f"     └── /{name:10} → http://localhost:{port}/ui")
    print(f"  📁 Dashboard: {dashboard_dir}/index.html")
    print(f"{'='*60}")


