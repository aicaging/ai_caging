def setup_acl(parent_name: str, child_home: Path):
    """Set ACL so parent can access child's home (replaces setfacl in init.sh)."""
    if not child_home.exists():
        print(f"  SKIP ACL: {child_home} does not exist")
        return
    try:
        subprocess.run(
            ["setfacl", "-m", f"u:{parent_name}:rx", str(child_home)],
            check=True, capture_output=True,
        )
        print(f"  ACL: {parent_name} can access {child_home}")
    except subprocess.CalledProcessError as e:
        print(f"  ACL skipped (setfacl not available?): {e.stderr.decode().strip()}")


